    #[test]
    fn test_spake2_handshake_wrong_passphrase() {
        let (owner_state, _owner_msg) = start_owner_handshake("correct-phrase");
        let (_invitee_state, invitee_msg) = start_invitee_handshake("wrong-phrase");

        // SPAKE2 will "succeed" but derive different keys
        // The mismatch is detected when decryption fails
        let owner_key = finish_handshake(owner_state, &invitee_msg);
        // Note: spake2 crate may return Ok with a different key, or Err
        // depending on the implementation. Either way, the keys won't match.
        if let Ok(key) = owner_key {
            // If it succeeds, verify the key is different from a correct handshake
            let (correct_owner, _correct_owner_msg) = start_owner_handshake("correct-phrase");
            let (_correct_invitee, correct_invitee_msg) =
                start_invitee_handshake("correct-phrase");
            let correct_key = finish_handshake(correct_owner, &correct_invitee_msg).unwrap();
            assert_ne!(key, correct_key, "Wrong passphrase should produce different key");
        }
        // If Err, that's also correct behavior
    }

